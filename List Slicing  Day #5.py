# Basic Example
nums=[10,20,30,40,50]
print(nums[0:3])

# First N Elements
print(nums[:4])

# From Index to End
print(nums[3:])

# Full Copy
print(nums[:])

# Reverse a list
print(nums[::-1])

# Alternate Elements
print(nums[::2])

# Negative Indexing
print(nums[-4])
print(nums[-3:])

