# Upper
text="python programming"
print(text.upper())

# lower
text="I LOVE PROGRAMMING"
print(text.lower())

# Capitalize
name="aishwarya"
print(name.capitalize())

# strip
text="Hello    World   This   is    Aishwarya  Loves Python    "
print(text.strip())

# replace
text="Aishu Loves Dog"
print(text.replace("Dog", "Cat"))

# split
fruits="Apple,Orange,Banana"
print(fruits.split())


