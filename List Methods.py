# append
n=[10,20]
n.append(30)
n.append(40)
print(n)

# insert
n.insert(5,27)
print(n)

# remove
n.remove(27)
print(n)

#pop
n.pop(3)
print(n)

#sort
num=[5,3,1,2,4]
num.sort()
print(num)

#reverse
num.reverse()
print(num)

#count
sats=[1,2,2,3,3,4,4,4,5,5]
print(sats.count(4))
print(sats.count(2))

#index
print(sats.index(4)) 
